package de.jvcard.types;

public class vCardException extends Exception {

	private static final long serialVersionUID = 3688043061709402010L;

	public vCardException() {
		super();
	}

	public vCardException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public vCardException(String arg0) {
		super(arg0);
	}

	public vCardException(Throwable arg0) {
		super(arg0);
	}

}
